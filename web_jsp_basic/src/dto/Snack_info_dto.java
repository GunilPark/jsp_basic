package dto;



public class Snack_info_dto {
	
	private String m_code, m_name;

	public Snack_info_dto(String m_code, String m_name) {
		this.m_code = m_code;
		this.m_name = m_name;
	}

	public String getM_code() {
		return m_code;
	}

	public void setM_code(String m_code) {
		this.m_code = m_code;
	}

	public String getM_name() {
		return m_name;
	}

	public void setM_name(String m_name) {
		this.m_name = m_name;
	}

	
	
}
